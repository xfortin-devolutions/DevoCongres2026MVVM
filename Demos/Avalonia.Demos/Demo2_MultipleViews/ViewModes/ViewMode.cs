namespace Avalonia.Demos.Demo2_MultipleViews.ViewModes;

/// <summary>
/// Represents the different view modes for displaying the product catalog.
/// </summary>
public enum ViewMode
{
    /// <summary>
    /// Grid/table view with columns showing all product details.
    /// </summary>
    Grid,

    /// <summary>
    /// Compact tile view showing key product information.
    /// </summary>
    Tile,

    /// <summary>
    /// Full detail card view with spacious layout.
    /// </summary>
    Card
}
